//-----Operators in C-----

#include<stdio.h>

void main()
{
    int a=10;
    int b=20;
    printf("addition :%d\n",a+b);
    printf("subtraction :%d\n",b-a);
    printf("multiplication :%d\n",a*b);
    printf("division :%f \n",b/a);
    printf("division :%.3f \n",b/a);
    printf("remainder :%d\n",b%a);

}
